export interface MockProfile {
  id: string
  firstName: string
  lastName: string
  name: string
  email: string
  password: string
  avatar?: string
  bio: string
  gender: string
  age: number
  height: number
  city: string
  job: string
  hobbies: string[]
  languages: string[]
  education: string
  createdAt: string
}


function getRandomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function generateRandomAge(): number {
  return Math.floor(Math.random() * (35 - 18 + 1)) + 18
}

function generateRandomEmail(firstName: string, lastName: string): string {
  const domains = ['gmail.com', 'yahoo.com', 'hotmail.com', 'outlook.com', 'email.com']
  const cleanName = `${firstName}${lastName}`.toLowerCase().replace(/\s+/g, '')
  const domain = getRandomItem(domains)
  const randomNum = Math.floor(Math.random() * 9999)
  return `${cleanName}${randomNum}@${domain}`
}

function generateRandomAvatar(index: number): string {
  return `https://api.dicebear.com/7.x/avataaars/svg?seed=${index}&scale=80`
}

function generateRandomHeight(): number {
  return Math.floor(Math.random() * (185 - 155 + 1)) + 155 // 155-185cm
}

function getRandomItems<T>(arr: T[], count: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function generateMockProfile(index: number): MockProfile {
  const firstName = getRandomItem(vietnameseFirstNames)
  const lastName = getRandomItem(vietnameseLastNames)
  const gender = getRandomItem(genders)
  const name = `${lastName} ${firstName}`
  
  return {
    id: `mock-${index}`,
    firstName,
    lastName,
    name,
    email: generateRandomEmail(firstName, lastName),
    password: '123456',
    avatar: generateRandomAvatar(index),
    bio: getRandomItem(bioTemplates),
    gender,
    age: generateRandomAge(),
    height: generateRandomHeight(),
    city: getRandomItem(cities),
    job: getRandomItem(jobs),
    hobbies: getRandomItems(hobbies, Math.floor(Math.random() * 6) + 5),
    languages: getRandomItems(languages, Math.floor(Math.random() * 4) + 3),
    education: getRandomItem(educationLevels),
    createdAt: new Date(Date.now() - Math.random() * 90 * 24 * 60 * 60 * 1000).toISOString(),
  }
}

export function generateManyMockProfiles(count: number): MockProfile[] {
  return Array.from({ length: count }, (_, i) => generateMockProfile(i))
}
